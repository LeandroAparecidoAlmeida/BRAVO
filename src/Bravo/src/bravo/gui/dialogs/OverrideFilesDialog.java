    public OverrideFilesDialog(java.awt.Frame parent, String title, List<File> files) {
        super(parent, true);
        initComponents();
        setLocationRelativeTo(parent);
        setTitle(title);
        String[] columns = new String[] {"ARQUIVO"};       
        jTable1.setModel(new DefaultTableModel(columns, files.size()) {                
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            } 
        });
        for (int i = 0; i < files.size(); i++) {
            jTable1.setValueAt(files.get(i).getAbsolutePath(),  i, 0);                   
        }
        if (jTable1.getRowCount() > 0) {
            jTable1.setRowSelectionInterval(0, 0);          
        }
    }
    
    private void confirm() {
        override = true;
        setVisible(false);
    }
    
    private void cancel() {
        override = false;
        setVisible(false);
    }
    
    public boolean override() {
        return override;
    }

